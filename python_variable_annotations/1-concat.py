#!/usr/bin/env python3
"""
This module contains the add function that takes two strings
and concatenates them.
"""


def concat(str1: str, str2: str) -> str:
    """
    Returns a concatenated string.

    Args:
        str1 (string): The first string.
        str2 (string): The second String.

    Returns:
        string: A combination of two strings.
    """
    return str1 + str2
